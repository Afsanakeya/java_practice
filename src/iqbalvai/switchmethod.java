package iqbalvai;

public class switchmethod {

	public static void main(String[] args) {
		int age=5;
		switch(age) {
		case 1:
			System.out.println("you are big enough");
		break;
	
		case 2:
			
			System.out.println("you are big enough to talk");
			break;
		case 3:
			System.out.println("you can talk and walk");
			break;
		
		case 4:
		System.out.println("you can be in trouble");
		break;
		
		default:
			System.out.println("you are good for nothing");
		
		break;
		}
		
	
	
	
	}
	

}
